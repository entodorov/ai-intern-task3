#     да видиш дали работи?"

#     Ти: "Ръчното тестване е бавно и несигурно. Автоматизираните тестове ми гарантират, че ако 
#     утре променя нещо по функцията read_docx (например за да поддържа PDF файлове), няма да счупя 
#     четенето на Word файлове без да разбера. Те ме пазят от т.нар. регресии (regression bugs)."

# Менторът: "Защо в setUp създаваш нов (dummy) файл, вместо да използваш някой от вече съществуващите
#  документи в папка data/edamame/?"

#     Ти: "Защото един добър тест трябва да бъде независим и детерминистичен (deterministic). 
#     Ако разчитам на реалните файлове, утре някой може да изтрие папка data/ и тестът ми ще се счупи, 
#     въпреки че кодът ми е напълно верен. Като генерирам файла вътре в самия тест, аз контролирам на 
#     100% средата."

# Менторът: "Какво правят setUp и tearDown?"

#     Ти: "Това е жизненият цикъл (lifecycle) на теста. setUp подготвя сцената (създава данни), 
#     а tearDown почиства след нас (изтрива файла). Така дори и тестът да се провали по средата, 
#     tearDown гарантира, че няма да оставим 'боклуци' по харддиска."