# --- ИМПОРТИРАНЕ НА БИБЛИОТЕКИ ---
import unittest # Вградена библиотека в Python специално за писане на автоматизирани тестове
import docx     # За да можем да създадем фалшив Word документ за теста
import os       # За да изтрием файла след като приключим

# Импортираме САМО функцията, която искаме да тестваме, от нашия основен файл ingest.py
from ingest import read_docx

# Създаваме клас за нашите тестове. Той задължително наследява unittest.TestCase
class TestIngestion(unittest.TestCase):
    
    # --- СТЪПКА 1: ПОДГОТОВКА (Arrange) ---
    def setUp(self):
        """
        Методът setUp се изпълнява автоматично ПРЕДИ всеки един тест.
        Тук създаваме "контролирана среда". Вместо да четем реалните ти файлове от папката,
        ние създаваме чисто нов, временен Word файл само с 2 изречения.
        """
        self.test_file = "dummy_test_meeting.docx"
        doc = docx.Document()
        doc.add_paragraph("Това е тестова среща.")
        doc.add_paragraph("Обсъждаме изкуствен интелект.")
        doc.save(self.test_file) # Запазваме файла на харддиска

    # --- СТЪПКА 2: ДЕЙСТВИЕ И ПРОВЕРКА (Act & Assert) ---
    def test_read_docx(self):
        """
        Това е самият тест. (ВАЖНО: Името на функцията задължително трябва да започва с 'test_')
        Тук проверяваме дали нашата функция read_docx работи правилно.
        """
        # 1. Действие (Act): Пускаме нашата функция да прочете фалшивия файл
        extracted_text = read_docx(self.test_file)
        
        # 2. Проверка (Assert): Проверяваме дали резултатът е това, което очакваме.
        # assertIn проверява дали първият текст се съдържа във втория.
        # Ако текстът липсва, тестът "гърми" и ни казва, че имаме бъг в кода.
        self.assertIn("Това е тестова среща.", extracted_text)
        self.assertIn("Обсъждаме изкуствен интелект.", extracted_text)
        
        print("\n✅ ТЕСТЪТ МИНА УСПЕШНО! Функцията read_docx работи перфектно.")

    # --- СТЪПКА 3: ИЗЧИСТВАНЕ (Teardown) ---
    def tearDown(self):
        """
        Методът tearDown се изпълнява автоматично СЛЕД всеки тест, независимо дали е минал или се е счупил.
        Тук "почистваме" след себе си – изтриваме фалшивия файл, за да не ти пълним компютъра с боклук.
        """
        if os.path.exists(self.test_file):
            os.remove(self.test_file)

# Това стартира тестовете, ако пуснеш файла от терминала
if __name__ == '__main__':
    unittest.main()



