# --- ИМПОРТИРАНЕ НА БИБЛИОТЕКИ ---
import os
from dotenv import load_dotenv
from supabase import create_client

# --- СТЪПКА 1: Зареждане на конфигурацията ---
# Точно както в ingest.py, зареждаме тайните ключове от .env файла.
# Това е добра практика (DRY - Don't Repeat Yourself), защото ако утре сменим 
# базата данни, ще променим ключа само на едно място (.env), а и двата скрипта ще работят.
load_dotenv()
supabase = create_client(os.environ.get("SUPABASE_URL"), os.environ.get("SUPABASE_KEY"))

# --- СТЪПКА 2: Извличане на данните (Read / SELECT) ---
# Тук правим заявка към Supabase. 
# На езика на базите данни (SQL) това се превежда като: 
# SELECT id, title, source FROM meetings;
res = supabase.table("meetings").select("id, title, source").execute()

# --- СТЪПКА 3: Принтиране на резултата ---
# res.data е списък (list) от речници (dictionaries), който Supabase ни връща.
# Функцията len() ни казва колко точно записа има в този списък.
print(f"\nНамерени са {len(res.data)} срещи в базата данни:")

# Завъртаме цикъл (for loop), който минава през всяка една намерена среща
for m in res.data:
    # Принтираме я красиво форматирана: [Име на проекта] Заглавие на срещата
    # m['source'] взима проекта (напр. edamame), а m['title'] взима заглавието
    print(f"- [{m['source']}] {m['title']}")



#     Менторът: "Защо не използваш чист SQL (като SELECT * FROM meetings), а пишеш supabase.table().select()?"

#     Ти: "Supabase предоставя SDK (Software Development Kit), което работи като ORM (Object-Relational 
#     Mapping). То превръща Python методите директно в сигурни REST API заявки към базата. Това ме 
#     предпазва от често срещани хакерски атаки като SQL Injection и прави кода много по-четим и лесен з
#     а поддръжка."

# Менторът: "Защо изобщо имаме отделен файл query.py? Не можеше ли просто да принтираш данните накрая 
# в ingest.py?"

#     Ти: "Можеше, но това нарушава принципа за Separation of Concerns (Разделяне на отговорностите). 
#     ingest.py отговаря само за вкарването на данните (Write). query.py отговаря само за четенето 
#     (Read). Ако ги смеся, скриптът ще стане тромав и труден за тестване. Освен това query.py служи 
#     като независим инструмент за проверка (sanity check)."

# Менторът: "Какво ще върне .execute(), ако таблицата meetings е напълно празна?"

#     Ти: "Няма да даде грешка. Ще върне празен списък ([]) в res.data. Кодът ми е защитен срещу това, 
#     защото len([]) ще бъде 0 и цикълът for просто няма да се изпълни нито веднъж, а в конзолата ще 
#     изпише 'Намерени са 0 срещи'."